    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        try {
            /*
                Checks if the user name and password are valid
                If no error message appears.
            */
            if (sp.login(username.getText(), password.getPassword())) {
                setVisible(false);
                java.awt.EventQueue.invokeLater(() -> {
                    new AfterLogin().setVisible(true);
                });
            } else {
                //Dialog window with an error message
                JOptionPane.showMessageDialog(rootPane, "You enter a wrong combination\npassword/username");
                setVisible(false);
                System.exit(0);
            }
        } catch (RemoteException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_loginActionPerformed

    public static void main(String args[]) {
        try {
            //Try to locate a registry, bind on a port 1100
            registry = LocateRegistry.getRegistry(1100);
            try {
                //Look for a specific method
                sp = (ServiceProvider) registry.lookup("ClientServiceUtillity");
            } catch (NotBoundException | AccessException ex) {
                ex.printStackTrace();
            }
            //Display a IU for login form
            java.awt.EventQueue.invokeLater(() -> {
                new ClientLogin().setVisible(true);
            });
        } catch (RemoteException ex) {
            ex.printStackTrace();
        }
    }

